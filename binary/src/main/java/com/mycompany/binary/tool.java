    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String text = jTextArea1.getText();
        String[] lines = text.split("\\n");
        int[] arr = new int[lines.length];
        for (int i = 0; i < lines.length; i++) {
            try {
                arr[i] = Integer.parseInt(lines[i].trim());
            } catch (NumberFormatException ex) {

                System.err.println("Invalid number format at line " + (i + 1));
            }
        }
// Binary Search Part
        int target = Integer.parseInt(jTextField1.getText());
        int result = Bfind(arr, target, 0, arr.length - 1);

        if (result != -1) {
            jTextField2.setText(String.valueOf(result));
        } else {
            jTextField2.setText("Not found this element");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

